-- ============================================
-- V42: 术语映射表 lg_terminology_mapping
-- 将原先硬编码在 application.yml 的中文业务术语 → 英文子词映射迁入数据库，
-- 以后新增术语直接入库即可，无需改代码/改配置重新部署。
-- 服务层（ConfigurableTerminologyService）启动时加载全表并建内存缓存加速匹配。
-- ============================================

CREATE TABLE IF NOT EXISTS lg_terminology_mapping (
    id            UUID PRIMARY KEY,
    source_term   VARCHAR(128) NOT NULL,
    target_terms  JSONB NOT NULL DEFAULT '[]'::JSONB,
    description   VARCHAR(512),
    status        VARCHAR(32) NOT NULL DEFAULT 'ACTIVE',
    sort_order    INTEGER NOT NULL DEFAULT 0,
    deleted       SMALLINT NOT NULL DEFAULT 0,
    created_at    TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at    TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    UNIQUE(source_term)
);

CREATE INDEX IF NOT EXISTS idx_lg_terminology_mapping_status
    ON lg_terminology_mapping(status);

COMMENT ON TABLE lg_terminology_mapping IS '中文业务术语 → 英文子词映射表（跨语言名称匹配用）';
COMMENT ON COLUMN lg_terminology_mapping.source_term IS '源术语（如中文业务术语「出金」）';
COMMENT ON COLUMN lg_terminology_mapping.target_terms IS '目标英文子词列表（camelCase 拆分后的形式），JSONB 数组';
COMMENT ON COLUMN lg_terminology_mapping.status IS '状态：ACTIVE 生效 / DISABLED 停用';

